package main

import "fmt"

func main() {
	date := "6 червня 22р"
	fmt.Println(date, "Придумати сценарій")
	fmt.Println(date, "Зняти демку")
	fmt.Println(date, "Скинути демку і сценарій пацанам, аби глянули")
	fmt.Println(date, "Переробити")
	fmt.Println(date, "Cкинути демку ше раз, для погодження")
	fmt.Println(date, "Получили погодження")

	date = "23 липня 22р"
	fmt.Println(date, "Зняти відос")
	fmt.Println(date, "Змонтувати відос")
}
